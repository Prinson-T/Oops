public class Arrays {
    public static void main(String[] args) {
        int a[]=new int[5];
        a[0]=40;
        a[1]=50;
        a[2]=55;
        a[3]=60;
        a[4]=80;
        System.out.println(a[0]+" "+a[4]);
        for (int i=0;i<5;i++){
            System.out.println(a[i]);
        }
    }
}
